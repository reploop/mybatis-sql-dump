    Stack<Column> columns = new Stack<>();

    public Stack<Table> getTables() {
        return tables;
    }

    public Stack<Column> getColumns() {
        return columns;
    }

    private <K> void acceptIfPresent(K node, BiConsumer<K, JoinVisitor> consumer) {
        if (null != node) {
            consumer.accept(node, this);
        }
    }

    private <K> void acceptAllIfPresent(Collection<K> nodes, BiConsumer<K, JoinVisitor> consumer) {
        if (null != nodes) {
            nodes.forEach(node -> acceptIfPresent(node, consumer));
        }
    }

    @Override
    public void visit(PlainSelect plainSelect) {
        acceptIfPresent(plainSelect.getFromItem(), FromItem::accept);
        List<Join> joins = plainSelect.getJoins();
        if (null != joins) {
            for (Join join : joins) {
                acceptIfPresent(join.getRightItem(), FromItem::accept);
                acceptAllIfPresent(join.getOnExpressions(), Expression::accept);
                var columns = join.getUsingColumns();
                acceptAllIfPresent(columns, Column::accept);
            }
        }
        acceptIfPresent(plainSelect.getWhere(), Expression::accept);
    }

